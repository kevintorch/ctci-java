package moderate.Q16_sub_sort;

public class Indices {
    int startIndex;
    int endIndex;

    public Indices(int startIndex, int endIndex) {
        this.startIndex = startIndex;
        this.endIndex = endIndex;
    }

    @Override
    public String toString() {
        return "Indices{" +
                "startIndex=" + startIndex +
                ", endIndex=" + endIndex +
                '}';
    }
}
